#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	t;

	i = 0;
	t = argc -1;
	if (argc > 0)
	{
		while (t > 0)
		{
			while (argv[t][i] != '\0')
			{
				write (1, (&argv[t][i]), 1);
				i++;
			}
			write (1, "\n", 1);
			t--;
			i = 0;
		}
	}
}
